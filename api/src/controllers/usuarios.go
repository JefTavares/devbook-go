// CriarUsuario - Insere um usuário no banco de dados
func CriarUsuario(w http.ResponseWriter, r *http.Request) {
	//corpoRequest, err := ioutil.ReadAll(r.Body) ioutil deprecation se use IO ou OS
	corpoRequest, err := io.ReadAll(r.Body)
	if err != nil {
		respostas.Erro(w, http.StatusUnprocessableEntity, err)
		return
	}

	var usurio modelos.Usuario
	if err = json.Unmarshal(corpoRequest, &usurio); err != nil {
		respostas.Erro(w, http.StatusBadRequest, err)
		return
	}

	if err = usurio.Preparar("cadastro"); err != nil {
		respostas.Erro(w, http.StatusBadRequest, err)
		return
	}

	db, err := banco.Conectar()
	if err != nil {
		respostas.Erro(w, http.StatusInternalServerError, err)
		return
	}
	defer db.Close()

	repositorio := repositorios.NovoRepositorioDeUsuario(db)
	usurio.ID, err = repositorio.Criar(usurio)
	if err != nil {
		respostas.Erro(w, http.StatusInternalServerError, err)
		return
	}

	respostas.JSON(w, http.StatusCreated, usurio)
	//w.Write([]byte(fmt.Sprintf("ID inserido: %d", usuarioId)))
}

